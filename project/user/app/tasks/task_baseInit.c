#include "common.h"

/* 基础驱动初始化 */
void task_baseInit(void)
{
    _INIT; /* 初次进入任务时，执行一次 */

    sl_goto(task_flow);

    _FREE; /* 任务结束，不再执行时，释放资源 */

    _RUN; /* 下方开始进入任务运行逻辑 */
}

